//Q24. Write a Java Program to print current time in the format HH:MM:SS.
import java.util.*;
class q24 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String HH = sc.next();
        String MM = sc.next();
        String SS = sc.next();
        System.out.println(HH + "/" + MM + "/" + SS);
    }
} 